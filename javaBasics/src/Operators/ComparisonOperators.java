package Operators;

public class ComparisonOperators {
	public static void main(String[]args) {
		
		int a=5;
		int b=10;
		
		// '>' (greater than)
		System.out.println(a>b);
		
		// '>=' (greater than and equal to)
		System.out.println(a>=b);
		
		// '<=' (lesser than and equal to)
		System.out.println(a<=b);
		
		// '<' (lesser than)
		System.out.println(a<b);
		
		// '=='  (equal to)
		System.out.println(a==b);
		
		// '!='  (not equal to)
		System.out.println(a!=b);
		
		
	}

}
