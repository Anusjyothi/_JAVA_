package Operators;

public class AssignmentOperator {
	public static void main(String[]args) {
		
		int a=10;
		a+=5; //-->a=a+5-->a=10+5
		System.out.println(a);
		
		int b=8;
		b-=5; //-->b=b-5-->b=8-5
		System.out.println(b);
		
		int c=10;
		c*=10; //-->c=c*10-->c=10*10
		System.out.println(c);
		
		float d=5;
		d/=2; //-->d=d/2-->d=5/2
		System.out.println(d);
		
		int e=8;
		e%=4; //e=e%4-->e=8%4
		System.out.println(e);
		
		
		
		
		
		
		
		
		
		
		
	}

}
