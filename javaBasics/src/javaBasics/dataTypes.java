package javaBasics;

public class dataTypes {
	public static void main(String[]args) {
		
				//Initialisation of primitive Data Types
				int myNumber=18; //integer
				boolean status=true;//true or false here 't' and 'f' small letters
				char Alphabet='A'; //
				float f1=1.80f;
				float f2=2;
				long longs=123356464l;
				double d=4641612.1646451664;
				short s=12345;
				byte b=12;
				
				System.out.println(myNumber);
				System.out.println(status);
				System.out.println(Alphabet);
				System.out.println(f1); //here,appended f in the value because this float value has decimal point
				System.out.println(f2); //here, not appended f as it does'nt have any decimal point, it's just a whole number..!
				System.out.println(longs);//here we must append value l
				System.out.println(d);
				System.out.println(s);
				System.out.println(b);
	}
	
	

}
